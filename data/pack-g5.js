/* ---- 5年専用の小道具：小数を整数のまま計算して文字にする（浮動小数の誤差よけ） ---- */
function g5ds(n,p){
  /* n×10^(-p) を文字列に。例 g5ds(35,1)="3.5"・g5ds(30,1)="3"・g5ds(234,2)="2.34" */
  let s=String(n);
  if(p>0){
    while(s.length<=p) s="0"+s;
    s=s.slice(0,-p)+"."+s.slice(-p);
    s=s.replace(/0+$/,"").replace(/\.$/,"");
  }
  return s;
}
function g5qc(text, right, wrongs, cat){
  /* 正解＋まちがい候補から 4択をつくる（かぶりは自動で除外） */
  const set=new Set([right]);
  for(const w of wrongs){ if(set.size>=4) break; if(w!=null && w!==right) set.add(w); }
  return qc(text, right, [...set], cat);
}

